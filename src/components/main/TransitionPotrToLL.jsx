// 작은 로고(240px) → SVG(560px): scale = 240/560 ≈ 0.43
const INIT_SCALE = 240 / 560;

const TransitionPotrToLL = () => {
    const sectionRef = useRef(null);
    const bgRef = useRef(null);
    const leftTopRef = useRef(null);
    const rightImgTopRef = useRef(null);
    const letterRefs = useRef([]);

    useEffect(() => {
        const section = sectionRef.current;
        const bgEl = bgRef.current;
        const leftTop = leftTopRef.current;
        const rightImgTop = rightImgTopRef.current;
        const letters = letterRefs.current.filter(Boolean);

        if (!section || !bgEl || !leftTop || !rightImgTop || letters.length === 0) return;

        gsap.set([leftTop, rightImgTop], { clipPath: 'inset(0 0 100% 0)' });
        letters.forEach((el) => {
            gsap.set(el, {
                scale: INIT_SCALE,
                svgOrigin: '0 103',
                force3D: true,
            });
        });

        let tl;

        const ctx = gsap.context(() => {
            const sectionH = section.offsetHeight;

            const getRelativeRect = (el) => {
                const sRect = section.getBoundingClientRect();
                const eRect = el.getBoundingClientRect();
                const top = eRect.top - sRect.top;
                return { top, bottom: top + eRect.height, height: eRect.height };
            };

            const leftCache = getRelativeRect(leftTop);
            const rightImgCache = getRelativeRect(rightImgTop);

            const updateClip = (el, cache, bgTopEdge) => {
                let pct;
                if (bgTopEdge >= cache.bottom) pct = 0;
                else if (bgTopEdge <= cache.top) pct = 100;
                else pct = ((cache.bottom - bgTopEdge) / cache.height) * 100;
                gsap.set(el, { clipPath: `inset(0 0 ${pct}% 0)` });
            };

            tl = gsap.timeline({
                scrollTrigger: {
                    trigger: section,
                    start: 'bottom bottom',
                    end: '+=1200',
                    scrub: 2,
                    pin: true,
                    pinSpacing: true,
                    anticipatePin: 1,
                    invalidateOnRefresh: true,
                    onRefresh() {
                        Object.assign(leftCache, getRelativeRect(leftTop));
                        Object.assign(rightImgCache, getRelativeRect(rightImgTop));
                    },
                },
            });

            // ① 노란 배경 슬라이드 + 이미지 clip
            tl.fromTo(
                bgEl,
                { yPercent: 100 },
                {
                    yPercent: 0,
                    duration: 1,
                    ease: 'none', // ✅ scrub 애니메이션에서는 ease를 'none'으로 주는 것이 스크롤과 일체감이 높습니다.
                    force3D: true,
                    onUpdate() {
                        const yPct = gsap.getProperty(bgEl, 'yPercent');
                        const bgTopEdge = (yPct / 100) * section.offsetHeight; // sectionH 대신 직접 참조
                        updateClip(leftTop, leftCache, bgTopEdge);
                        updateClip(rightImgTop, rightImgCache, bgTopEdge);
                    },
                },
                0
            );

            // ② 글자 stagger: 작은 크기 → 원래 크기
            tl.to(
                letters,
                {
                    scale: 1,
                    svgOrigin: '0 103',
                    duration: 0.6, // duration을 살짝 줄여서 더 빠르게 완성되도록 합니다.
                    stagger: {
                        each: 0.03,
                        from: 'end',
                    },
                    ease: 'power2.out', // 좀 더 탄력 있는 느낌
                    force3D: true,
                },
                0.05 // ✅ 0.1에서 0.05로 당김
            );
        }, sectionRef.current);

        // Resolve scroll trigger miss-calculation due to late image load
        const handleRefresh = () => ScrollTrigger.refresh();
        window.addEventListener('load', handleRefresh);
        const timeoutId = setTimeout(handleRefresh, 500);

        return () => {
            ctx.revert();
            window.removeEventListener('load', handleRefresh);
            clearTimeout(timeoutId);
        };
    }, []);

    return (
        <section className="tp2ll-section" ref={sectionRef}>
            {/* 노란 배경 (LLSection) */}
            <div className="tp2ll-bg" ref={bgRef} />

            {/* PotrSection bottom-content 레이아웃 */}
            <div className="tp2ll-content">
                {/* 좌측: 이미지(452×300) + 로고 */}
                <div className="tp2ll-left">
                    <div className="tp2ll-small-img">
                        <div className="tp2ll-img-layer">
                            <img src="/images/main/mainluggageimage1.png" alt="Luggage Label" />
                        </div>
                        <div className="tp2ll-img-layer tp2ll-img-layer--clip" ref={leftTopRef}>
                            <img src="/images/main/mainpotrimage6.png" alt="Potr" />
                        </div>
                    </div>

                    {/* SVG 로고: 작은 크기로 시작 → stagger 커짐 */}
                    <div className="tp2ll-logo-area">
                        <svg
                            width="560"
                            height="103"
                            viewBox="0 0 560 103"
                            fill="none"
                            xmlns="http://www.w3.org/2000/svg"
                            className="tp2ll-logo-svg"
                        >
                            {LETTERS.map((letter, i) => (
                                <g key={letter.id} ref={(el) => (letterRefs.current[i] = el)}>
                                    <path d={letter.d} fill="#838E4F" />
                                </g>
                            ))}
                        </svg>
                    </div>
                </div>

                {/* 우측: 이미지 1060×680 */}
                <div className="tp2ll-right">
                    <div className="tp2ll-large-img">
                        <div className="tp2ll-img-layer">
                            <img src="/images/main/mainluggageimage2.png" alt="Luggage Label" />
                        </div>
                        <div className="tp2ll-img-layer tp2ll-img-layer--clip" ref={rightImgTopRef}>
                            <img src="/images/main/mainpotrimage7.png" alt="Potr" />
                        </div>
                    </div>
                </div>
            </div>
        </section>
    );
};

export default TransitionPotrToLL;
